let currentLang = "de";

/* ===== LANGUAGE SWITCHING ===== */
function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            // Keep SVG icons inside buttons
            const svg = el.querySelector("svg");
            if (svg) {
                el.innerHTML = "";
                el.appendChild(svg);
                el.insertAdjacentHTML("beforeend", " " + translations[lang][key]);
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update language toggle label
    document.getElementById("langFlag").textContent = langLabels[lang] || lang.toUpperCase();

    // Update active state in dropdown
    document.querySelectorAll(".lang-option").forEach(btn => {
        btn.classList.toggle("active", btn.dataset.lang === lang);
    });

    // Update html lang attribute
    document.documentElement.lang = lang === "be" ? "nl-BE" : lang;

    // Update nav labels
    document.querySelectorAll(".side-nav-item").forEach(item => {
        const key = item.getAttribute("data-i18n-nav");
        if (key && translations[lang] && translations[lang][key]) {
            item.textContent = translations[lang][key];
        }
    });

    // Save preference
    try { localStorage.setItem("matchbite-lang", lang); } catch(e) {}
}

